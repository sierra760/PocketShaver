SRC = os.path.normpath(
    os.path.join(_HERE, "..", "..", "..", "..", "..", "Unix", "dyngen_precompiled"))
GEN_RE = re.compile(r"^DEFINE_GEN\((gen_op_[A-Za-z0-9_]+),void,\(([^)]*)\)\)")

