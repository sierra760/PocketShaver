REAL["gen_op_invoke_CPU_im_im"] = invoke("\temit_32(a64_mov_x(0, A64_CPU));\n\ta64_emit_mov_imm32(*this, 1, (uint32)param2);\n\ta64_emit_mov_imm32(*this, 2, (uint32)param3);\n")
REAL["gen_op_invoke_CPU_A0_ret_A0"] = invoke("\temit_32(a64_mov_x(0, A64_CPU));\n\temit_32(a64_mov_x(1, A64_T0));\n", "\temit_32(a64_mov_x(A64_T0, 0));\n")
for name in [n for n in list(REAL) if n.startswith("gen_op_invoke")]:
    REAL[name.replace("gen_op_invoke", "gen_op_invoke_direct", 1)] = REAL[name]

