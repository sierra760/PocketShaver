# ------------------------------------------------------------------ memory ops
# EA = T1 + OFFSET computed with W-form arithmetic (guest addresses wrap mod
# 2^32). Under MEM_BULK the access itself is emitted inline (fastmem): flat
# [VMBASE, EA(uxtw)] plus REV byteswap behind the same coarse pre-filter
# vm.hpp uses (EA >> 19 >= 0xbff covers both kernel-data windows and
# everything past the mapped guest space); the filter's cold path calls the
# old kpx_vm_* helpers, which apply the exact translation rules. Without
# MEM_BULK every access takes the helper call as before.

def _ea_w0(kind):
    # Effective address into w0 (the helper's first argument)
    if kind == "0":
        return [E("a64_mov_w(0, A64_T1)")]
    if kind == "T2":
        return [E("a64_add_reg(0, A64_T1, A64_T2)")]
    # im: D-form displacement, sign-extended; prefer single-insn imm12 forms
    return ["if ((uint32)param1 == 0) emit_32(a64_mov_w(0, A64_T1));",
            "else if ((uint32)param1 < 4096) emit_32(a64_add_imm(0, A64_T1, (uint32)param1));",
            "else if ((uint32)-(uint32)param1 < 4096) emit_32(a64_sub_imm(0, A64_T1, (uint32)-(uint32)param1));",
            "else { a64_emit_mov_imm32(*this, A64_X16, (uint32)param1); emit_32(a64_add_reg(0, A64_T1, A64_X16)); }"]

def _helper_body(pre, helper, tail=()):
    return "".join("\t" + l + "\n" for l in
                   list(pre) + ["a64_emit_call(*this, (uintptr)&%s);" % helper] + list(tail))

def _fastmem_body(pre, hot, helper, tail_cold=(), tail_shared=()):
    # pre (EA in w0, value in w1/x1 for stores) ; coarse filter ; hot access
    # ; skip over the 5-insn cold helper call ; shared tail. Branch offsets
    # are exact instruction counts of this fixed shape.
    h, tc = len(hot), len(tail_cold)
    lines = list(pre)
    lines += [E("a64_lsr_imm(A64_X16, 0, 19)"),
              E("a64_cmp_imm(A64_X16, 0xbff)"),
              E("a64_b_cond(A64_HS, %d)" % ((h + 2) * 4))]
    lines += hot
    lines += [E("a64_b(%d)" % ((5 + tc + 1) * 4))]
    lines += ["a64_emit_call(*this, (uintptr)&%s);" % helper]
    lines += list(tail_cold)
    lines += list(tail_shared)
    return ("#ifdef KPX_JIT_INSTRUMENT\n"
            "\t{ extern unsigned long kpx_jit_inline_mem_emitted; kpx_jit_inline_mem_emitted++; }\n"
            "#endif\n" + "".join("\t" + l + "\n" for l in lines))

def _mem_bulk_gated(fast, legacy):
    return "#ifdef MEM_BULK\n" + fast + "#else\n" + legacy + "#endif\n"

MEM_READ = {"u8": (1, "kpx_vm_read_1", None), "s8": (1, "kpx_vm_read_1", "a64_sxtb"),
            "u16": (2, "kpx_vm_read_2", None), "s16": (2, "kpx_vm_read_2", "a64_sxth"),
            "u32": (4, "kpx_vm_read_4", None), "s32": (4, "kpx_vm_read_4", None)}
MEM_WRITE = {"8": (1, "kpx_vm_write_1"), "16": (2, "kpx_vm_write_2"), "32": (4, "kpx_vm_write_4")}

READ_HOT = {1: [E("a64_ldrb_uxtw(0, A64_VMBASE, 0)")],
            2: [E("a64_ldrh_uxtw(0, A64_VMBASE, 0)"), E("a64_rev16(0, 0)")],
            4: [E("a64_ldr_w_uxtw(0, A64_VMBASE, 0)"), E("a64_rev(0, 0)")]}
WRITE_HOT = {1: [E("a64_strb_uxtw(1, A64_VMBASE, 0)")],
             2: [E("a64_rev16(A64_X16, 1)"), E("a64_strh_uxtw(A64_X16, A64_VMBASE, 0)")],
             4: [E("a64_rev(A64_X16, 1)"), E("a64_str_w_uxtw(A64_X16, A64_VMBASE, 0)")]}

def mem(name):
    m = re.match(r"gen_op_load_(u8|s8|u16|s16|u32|s32)_T0_T1_(0|im|T2)$", name)
    if m:
        sz, helper, ext = MEM_READ[m.group(1)]
        tail = [E("%s(A64_T0, 0)" % ext)] if ext else [E("a64_mov_w(A64_T0, 0)")]
        ea = _ea_w0(m.group(2))
        return _mem_bulk_gated(_fastmem_body(ea, READ_HOT[sz], helper, (), tail),
                               _helper_body(ea, helper, tail))
    m = re.match(r"gen_op_store_(8|16|32)_T0_T1_(0|im|T2)$", name)
    if m:
        sz, helper = MEM_WRITE[m.group(1)]
        pre = _ea_w0(m.group(2)) + [E("a64_mov_w(1, A64_T0)")]
        return _mem_bulk_gated(_fastmem_body(pre, WRITE_HOT[sz], helper),
                               _helper_body(pre, helper))
    m = re.match(r"gen_op_(lmw|stmw)_T0_(\d+|im)$", name)
    if m:
        h = "kpx_op_" + m.group(1)
        r = ("a64_emit_mov_imm32(*this, 2, (uint32)param1);" if m.group(2) == "im"
             else E("a64_movz_w(2, %s, 0)" % m.group(2)))
        return emit(E("a64_mov_x(0, A64_CPU)"), E("a64_mov_w(1, A64_T0)"), r,
                    "a64_emit_call(*this, (uintptr)&%s);" % h)
    if name == "gen_op_lwarx_T0_T1":
        return emit(E("a64_mov_x(0, A64_CPU)"), E("a64_mov_w(1, A64_T1)"),
                    "a64_emit_call(*this, (uintptr)&kpx_op_lwarx);", E("a64_mov_w(A64_T0, 0)"))
    if name == "gen_op_stwcx_T0_T1":
        return emit(E("a64_mov_x(0, A64_CPU)"), E("a64_mov_w(1, A64_T0)"), E("a64_mov_w(2, A64_T1)"),
                    "a64_emit_call(*this, (uintptr)&kpx_op_stwcx);")
    if name == "gen_op_dcbz_T0":
        return emit(E("a64_mov_w(0, A64_T0)"), "a64_emit_call(*this, (uintptr)&kpx_op_dcbz);")
    return None

# ------------------------------------------------------------------ branch ops
# prep_branch_bo_*: inputs T1 = CR bit (0/1, conditional variants only);
# outputs T1 = taken flag (exactly 0/1), T2 = decremented CTR (decrement
# variants, stored to CPU state). Must not touch T0 (BCLR/BCCTR target).

_DEC = [E("a64_ldr_w(A64_T2, A64_CPU, (uint32)kpx_jit_ctr_offset(cpu()))"),
        E("a64_sub_imm(A64_T2, A64_T2, 1)"),
        E("a64_str_w(A64_T2, A64_CPU, (uint32)kpx_jit_ctr_offset(cpu()))")]

def _prep(dec, ctr_cond, cond_cond):
    # T1 = [ctr test] && [cond test]; each cond is an A64 cc applied to a
    # compare-with-zero of T2 (ctr) / T1 (cr bit)
    lines = list(_DEC) if dec else []
    if ctr_cond and cond_cond:
        lines += [E("a64_cmp_imm(A64_T2, 0)"), E("a64_cset(A64_X16, %s)" % ctr_cond),
                  E("a64_cmp_imm(A64_T1, 0)"), E("a64_cset(A64_X17, %s)" % cond_cond),
                  E("a64_and_reg(A64_T1, A64_X16, A64_X17)")]
    elif ctr_cond:
        lines += [E("a64_cmp_imm(A64_T2, 0)"), E("a64_cset(A64_T1, %s)" % ctr_cond)]
    elif cond_cond:
        lines += [E("a64_cmp_imm(A64_T1, 0)"), E("a64_cset(A64_T1, %s)" % cond_cond)]
    else:
        lines += [E("a64_movz_w(A64_T1, 1, 0)")]
    return "".join("\t" + l + "\n" for l in lines)

PREP_BRANCH = {
    # suffix: (decrement, ctr condition, cond condition) — cond tests the CR
    # bit in T1 (NE = branch-if-true, EQ = branch-if-false); ctr tests the
    # decremented CTR in T2 (NE = branch-if-ctr-nonzero, EQ = if-ctr-zero)
    "0000": (True,  "A64_NE", "A64_EQ"),
    "0001": (True,  "A64_EQ", "A64_EQ"),
    "001x": (False, None,     "A64_EQ"),
    "0100": (True,  "A64_NE", "A64_NE"),
    "0101": (True,  "A64_EQ", "A64_NE"),
    "011x": (False, None,     "A64_NE"),
    "1x00": (True,  "A64_NE", None),
    "1x01": (True,  "A64_EQ", None),
    "1x1x": (False, None,     None),
}

def branches(name):
    m = re.match(r"gen_op_prep_branch_bo_(\w+)$", name)
    if m and m.group(1) in PREP_BRANCH:
        dec, ctr_c, cond_c = PREP_BRANCH[m.group(1)]
        return _prep(dec, ctr_c, cond_c)
    if name == "gen_op_branch_1_T0":
        return emit(E("a64_str_w(A64_T0, A64_CPU, (uint32)kpx_jit_pc_offset(cpu()))"))
    if name == "gen_op_branch_1_im":
        return emit("a64_emit_mov_imm32(*this, A64_X16, (uint32)param1);",
                    E("a64_str_w(A64_X16, A64_CPU, (uint32)kpx_jit_pc_offset(cpu()))"))
    if name == "gen_op_branch_2_T0_im":    # pc = T1 ? T0 : PARAM1
        return emit("a64_emit_mov_imm32(*this, A64_X16, (uint32)param1);",
                    E("a64_cmp_imm(A64_T1, 0)"),
                    E("a64_csel(A64_X16, A64_T0, A64_X16, A64_NE)"),
                    E("a64_str_w(A64_X16, A64_CPU, (uint32)kpx_jit_pc_offset(cpu()))"))
    if name == "gen_op_branch_2_im_im":    # pc = T1 ? PARAM1 : PARAM2
        return emit("a64_emit_mov_imm32(*this, A64_X16, (uint32)param1);",
                    "a64_emit_mov_imm32(*this, A64_X17, (uint32)param2);",
                    E("a64_cmp_imm(A64_T1, 0)"),
                    E("a64_csel(A64_X16, A64_X16, A64_X17, A64_NE)"),
                    E("a64_str_w(A64_X16, A64_CPU, (uint32)kpx_jit_pc_offset(cpu()))"))
    # Direct block chaining: deposit patchable B slots (retargeted to the
    # resolver trampolines at translate time, then to target entry points at
    # run time). Slots MUST be B imm26 — run-time retargets span the whole
    # translation cache, beyond CBZ's +/-1MB. The placeholder a64_b(0) is a
    # well-formed branch-to-self so the patcher can classify the site; the
    # translator always retargets it before the block is published.
    if name == "gen_op_branch_chain_1":
        return emit("jmp_addr[0] = code_ptr();",
                    E("a64_b(0)"))
    if name == "gen_op_branch_chain_2":
        return emit(E("a64_cbz_w(A64_T1, 8)"),   # T1==0 -> not-taken slot (fixed local skip, never patched)
                    "jmp_addr[0] = code_ptr();",
                    E("a64_b(0)"),               # taken -> li[0] (tpc)
                    "jmp_addr[1] = code_ptr();",
                    E("a64_b(0)"))               # not taken -> li[1] (npc)
    return None

# ------------------------------------------------------------------ floating point
# Value plan: d8 = F0, d9 = F1, d10 = F2, d11 = FD (callee-saved low halves).
# Arithmetic goes through kpx_fp_* helpers that replicate the interpreter's
# execute_fp_arith exactly (incl. the FPSCR[FPRF] classify); the FPSCR-less
# move ops (fmov/fabs/fneg/fnabs) emit inline.

FREG = {"F0": 8, "F1": 9, "F2": 10, "FD": 11}
FP_ARITH2 = ("fadd", "fadds", "fsub", "fsubs", "fmul", "fmuls", "fdiv", "fdivs")
FP_ARITH3 = ("fmadd", "fmadds", "fmsub", "fmsubs", "fnmadd", "fnmadds", "fnmsub", "fnmsubs")

def fp(name):
    m = re.match(r"gen_op_fmov_(FD|F[012])_(FD|F[012])$", name)
    if m: return emit(E("a64_fmov_d(%d, %d)" % (FREG[m.group(1)], FREG[m.group(2)])))
    if name == "gen_op_fabs_FD_F0": return emit(E("a64_fabs_d(11, 8)"))
    if name == "gen_op_fneg_FD_F0": return emit(E("a64_fneg_d(11, 8)"))
    if name == "gen_op_fnabs_FD_F0": return emit(E("a64_fabs_d(11, 8)"), E("a64_fneg_d(11, 11)"))
    m = re.match(r"gen_op_(\w+)_FD_F0_F1$", name)
    if m and m.group(1) in FP_ARITH2:
        return emit(E("a64_mov_x(0, A64_CPU)"), E("a64_fmov_d(0, 8)"), E("a64_fmov_d(1, 9)"),
                    "a64_emit_call(*this, (uintptr)&kpx_fp_%s);" % m.group(1), E("a64_fmov_d(11, 0)"))
    m = re.match(r"gen_op_(\w+)_FD_F0_F1_F2$", name)
    if m and m.group(1) in FP_ARITH3:
        return emit(E("a64_mov_x(0, A64_CPU)"), E("a64_fmov_d(0, 8)"), E("a64_fmov_d(1, 9)"),
                    E("a64_fmov_d(2, 10)"),
                    "a64_emit_call(*this, (uintptr)&kpx_fp_%s);" % m.group(1), E("a64_fmov_d(11, 0)"))
    m = re.match(r"gen_op_load_(F[012])_FPR(\d+)$", name)
    if m:
        return emit(E("a64_ldr_d(%d, A64_CPU, (uint32)kpx_jit_fpr_offset(cpu(), %s))"
                      % (FREG[m.group(1)], m.group(2))))
    m = re.match(r"gen_op_store_(FD|F[012])_FPR(\d+)$", name)
    if m:
        return emit(E("a64_str_d(%d, A64_CPU, (uint32)kpx_jit_fpr_offset(cpu(), %s))"
                      % (FREG[m.group(1)], m.group(2))))
    m = re.match(r"gen_op_load_(double|single)_FD_T1_(0|im|T2)$", name)
    if m:
        ea = _ea_w0(m.group(2))
        if m.group(1) == "double":
            fast = _fastmem_body(ea,
                                 [E("a64_ldr_x_uxtw(0, A64_VMBASE, 0)"), E("a64_rev_x(0, 0)")],
                                 "kpx_vm_read_8", (), [E("a64_fmov_d_x(11, 0)")])
            legacy = _helper_body(ea, "kpx_vm_read_8", [E("a64_fmov_d_x(11, 0)")])
        else:
            # (double)float is exactly FCVT, so lfs inlines fully; the cold
            # path keeps the C converter and lands its result via x0
            fast = _fastmem_body(ea,
                                 [E("a64_ldr_w_uxtw(0, A64_VMBASE, 0)"), E("a64_rev(0, 0)"),
                                  E("a64_fmov_s_w(0, 0)"), E("a64_fcvt_d_s(11, 0)")],
                                 "kpx_fp_load_single", [E("a64_fmov_d_x(11, 0)")], ())
            legacy = _helper_body(ea, "kpx_fp_load_single", [E("a64_fmov_d_x(11, 0)")])
        return _mem_bulk_gated(fast, legacy)
    m = re.match(r"gen_op_store_(double|single)_F0_T1_(0|im|T2)$", name)
    if m:
        pre = _ea_w0(m.group(2)) + [E("a64_fmov_x_d(1, 8)")]
        if m.group(1) == "double":
            fast = _fastmem_body(pre,
                                 [E("a64_rev_x(A64_X16, 1)"), E("a64_str_x_uxtw(A64_X16, A64_VMBASE, 0)")],
                                 "kpx_vm_write_8")
            return _mem_bulk_gated(fast, _helper_body(pre, "kpx_vm_write_8"))
        # stfs stays a helper on all configs: the out-of-range denormalization
        # cases (exponent outside 874..896) are NOT a plain FCVT
        return _helper_body(pre, "kpx_fp_store_single")
    return None

def body_for(name):
    if name in REAL:
        return REAL[name]
    for fn in (moves, alu, shifts, xer_ops, compares, cr_ops, ppc_plain, mem, branches, fp):
        b = fn(name)
        if b is not None:
            return b
    return None

