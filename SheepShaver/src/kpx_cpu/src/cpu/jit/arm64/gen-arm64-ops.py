#!/usr/bin/env python3
"""Generate the arm64 hand-written dyngen ops headers.

Emits real arm64 bodies for the whitelisted native ops (control primitives,
the scalar-integer compute core, memory, branches, and floating point);
every other op keeps a loud abort stub, unreachable while the translate
whitelist excludes it.
"""
import os
import re

# Resolve the x86_64 declaration surface relative to this script so the
# generator runs from any checkout location.
