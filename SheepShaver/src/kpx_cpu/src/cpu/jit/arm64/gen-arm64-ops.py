TREG = {"T0": "A64_T0", "T1": "A64_T1", "T2": "A64_T2"}

# ------------------------------------------------------------------ control primitives

def invoke(pre, post=""):
    return pre + "\ta64_emit_call(*this, (uintptr)param1);\n" + post

REAL = {}
REAL["gen_op_jmp_fast"] = ("\tconst intptr rel = (intptr)param1 - (intptr)code_ptr();\n"
                           "\temit_32(a64_b(rel));\n")
REAL["gen_op_jmp_slow"] = "\ta64_emit_jmp_abs(*this, (uintptr)param1);\n"
REAL["gen_op_jmp_A0"] = "\temit_32(a64_br(A64_T0));\n"
REAL["gen_op_mov_ad_A0_im"] = "\ta64_emit_mov_imm64(*this, A64_T0, (uint64)param1);\n"
REAL["gen_op_invoke"] = invoke("")
REAL["gen_op_invoke_T0"] = invoke("\temit_32(a64_mov_w(0, A64_T0));\n")
REAL["gen_op_invoke_T0_T1"] = invoke("\temit_32(a64_mov_w(0, A64_T0));\n\temit_32(a64_mov_w(1, A64_T1));\n")
REAL["gen_op_invoke_T0_T1_T2"] = invoke("\temit_32(a64_mov_w(0, A64_T0));\n\temit_32(a64_mov_w(1, A64_T1));\n\temit_32(a64_mov_w(2, A64_T2));\n")
REAL["gen_op_invoke_T0_ret_T0"] = invoke("\temit_32(a64_mov_w(0, A64_T0));\n", "\temit_32(a64_mov_w(A64_T0, 0));\n")
REAL["gen_op_invoke_im"] = invoke("\ta64_emit_mov_imm32(*this, 0, (uint32)param2);\n")
REAL["gen_op_invoke_CPU"] = invoke("\temit_32(a64_mov_x(0, A64_CPU));\n")
